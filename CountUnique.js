CountUnique = (array)=>{
    if(array.lenght>0){
        i=0;
        for(j=1; j<array.lenght; j++){
            if(array[i]!== array[j]){
                i++;
                array[i]=array[j]
            }
        }
        return i+1
    }else{
        throw new Error('array is empty')
    }
}


